#include <stdio.h>

int	ft_strcmp_exp(char *s1, char *s2)
{
	unsigned int	i;

	i = 0;
	while (s1[i] == s2[i] && s2[i] != '\0')
	{
		i++;
	}
	if (s2[i] == '\0' && (s1[i] == ' ' || (s1[i] >= 9 && s1[i] <= 13) || s1[i] == '\0' || s1[i] == '"'))
		return (0);
	return (1);
}
